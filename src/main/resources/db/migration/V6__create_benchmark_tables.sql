CREATE TABLE benchmarks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    description TEXT NOT NULL,
    type VARCHAR(20) NOT NULL,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE benchmark_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    benchmark_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    result VARCHAR(255) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_benchmark_results_benchmark FOREIGN KEY (benchmark_id) REFERENCES benchmarks(id) ON DELETE CASCADE
);

CREATE INDEX idx_benchmark_results_benchmark_id ON benchmark_results(benchmark_id);
CREATE INDEX idx_benchmark_results_user_id ON benchmark_results(user_id);
